package src.enums;

/**
 * Enumeration representing the difficulty or experience level of an internship.
 *
 * <p>Internship levels categorize the expected prerequisite skills and
 * experience required from applicants.
 */
public enum InternshipLevel {
    /**
     * Basic level internship. Suitable for students with minimal or no
     * prior internship or professional experience.
     */
    BASIC,

    /**
     * Intermediate level internship. Intended for students with some
     * prior internship or relevant coursework experience.
     */
    INTERMEDIATE,

    /**
     * Advanced level internship. Designed for experienced students with
     * multiple internships, strong technical skills, or relevant project
     * portfolio.
     */
    ADVANCED
}
